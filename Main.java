class SegTree{
    int len;
    int t[];
    SegTree(int l){
        this.len=l;
        t=new int[4*len];
    }
    void build(int arr[],int v,int tl,int tr){
        if(tl==tr){
            t[v]=arr[tr];
            return ;
        }
        int tm=(tl+tr)/2;
        build(arr,2*v, tl, tm);
        build(arr,2*v+1, tm+1, tr);
        t[v]=t[2*v]+t[2*v+1];
    }
    
    //This query is for sum change accordingly
    int query(int v,int tl,int tr,int l,int r){
        if(tl>r || tr<l) return 0;
        if(l<=tl && tr<=r) return t[v];
        int tm=(tl+tr)/2;
        int leftAns=query(2*v,tl,tm,l,r);
        int rightAns=query(2*v+1,tm+1,tr,l,r);
        return leftAns+rightAns;

    }

    void update(int v,int tl,int tr,int id,int val){
        if(tl==id && tr==id){
            t[v]=val;
            return;
        }
        if(id>tr || id<tl) return ;
        int tm=(tl+tr)/2;
        update(2*v,tl,tm,id,val);
        update(2*v+1,tm+1,tr,id,val);
        t[v]=t[2*v+1]+t[2*v];
    }

}

class DSU {
    int[] parent;
    int[] size;

    DSU(int n) {
        parent = new int[n + 1];
        size = new int[n + 1];
        for (int i = 0; i <= n; i++) {
            parent[i] = i;
            size[i] = 1;
        }
    }

    int find(int x) {
        if (parent[x] != x) {
            parent[x] = find(parent[x]); 
        }
        return parent[x];
    }

    void union(int x, int y) {
        int rootX = find(x);
        int rootY = find(y);
        if (rootX != rootY) {
            if (size[rootX] < size[rootY]) {
                int temp = rootX;
                rootX = rootY;
                rootY = temp;
            }
            parent[rootY] = rootX;
            size[rootX] += size[rootY];  
        }
    }

    int getSize(int x) {
        return size[find(x)];
    }
}

 
