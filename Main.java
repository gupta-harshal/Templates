    public class BinomialCoefficients {
	private static final int MAXN = (int)1e6;
	private static long[] fac = new long[MAXN + 1];
	private static long[] inv = new long[MAXN + 1];

	/** @return x^n modulo m in O(log p) time. */
	private static long exp(long x, long n, long m) {
		x %= m;  // note: m * m must be less than 2^63 to avoid ll overflow
		long res = 1;
		while (n > 0) {
			if (n % 2 == 1) { res = res * x % m; }
			x = x * x % m;
			n /= 2;
		}
		return res;
	}

	/** Precomputes n! from 0 to MAXN with a certain modulo. */
	private static void factorial(long p) {
		fac[0] = 1;
		for (int i = 1; i <= MAXN; i++) { fac[i] = fac[i - 1] * i % p; }
	}

	/**
	 * Precomputes all modular inverse factorials
	 * from 0 to MAXN in O(n + log p) time
	 */
	private static void inverses(long p) {
		inv[MAXN] = exp(fac[MAXN], p - 2, p);
		for (int i = MAXN; i >= 1; i--) { inv[i - 1] = inv[i] * i % p; }
	}

	/** @return nCr mod p */
	static long choose(long n, long r, long p) {
		return fac[(int)n] * inv[(int)r] % p * inv[(int)(n - r)] % p;
	}
}

class FastIO {
    private static final int BUFFER_SIZE = 1 << 16; // 64KB buffers
    private final DataInputStream din;
    private final byte[] buffer;
    private int bufferPointer, bytesRead;
    
    private final BufferedOutputStream out;
    private final byte[] outBuffer;
    private int outPointer;

    public FastIO() {
        din = new DataInputStream(System.in);
        buffer = new byte[BUFFER_SIZE];
        bufferPointer = bytesRead = 0;
        
        out = new BufferedOutputStream(System.out);
        outBuffer = new byte[BUFFER_SIZE];
        outPointer = 0;
    }

    // High-performance primitive integer reader (bypasses StringTokenizer)
    public int nextInt() throws IOException {
        int ret = 0;
        byte c = read();
        while (c <= ' ') {
            c = read();
        }
        boolean neg = (c == '-');
        if (neg) {
            c = read();
        }
        do {
            ret = ret * 10 + c - '0';
        } while ((c = read()) >= '0' && c <= '9');

        if (neg) return -ret;
        return ret;
    }

    // High-performance primitive long reader
    public long nextLong() throws IOException {
        long ret = 0;
        byte c = read();
        while (c <= ' ') {
            c = read();
        }
        boolean neg = (c == '-');
        if (neg) {
            c = read();
        }
        do {
            ret = ret * 10 + c - '0';
        } while ((c = read()) >= '0' && c <= '9');

        if (neg) return -ret;
        return ret;
    }

    // High-performance primitive double reader
    public double nextDouble() throws IOException {
        double ret = 0, div = 1;
        byte c = read();
        while (c <= ' ') {
            c = read();
        }
        boolean neg = (c == '-');
        if (neg) {
            c = read();
        }
        do {
            ret = ret * 10 + c - '0';
        } while ((c = read()) >= '0' && c <= '9');

        if (c == '.') {
            while ((c = read()) >= '0' && c <= '9') {
                ret += (c - '0') / (div *= 10);
            }
        }

        if (neg) return -ret;
        return ret;
    }

    // High-performance primitive character writer
    public void print(char c) throws IOException {
        if (outPointer == BUFFER_SIZE) {
            flushBuffer();
        }
        outBuffer[outPointer++] = (byte) c;
    }

    // High-performance primitive integer writer (No String allocations)
    public void print(int i) throws IOException {
        if (i == 0) {
            print('0');
            return;
        }
        if (i < 0) {
            print('-');
            i = -i;
        }
        int index = 0;
        byte[] temp = new byte[12];
        while (i > 0) {
            temp[index++] = (byte) ((i % 10) + '0');
            i /= 10;
        }
        while (index > 0) {
            print((char) temp[--index]);
        }
    }

    public void print(String str) throws IOException {
        for (int i = 0; i < str.length(); i++) {
            print(str.charAt(i));
        }
    }

    public void println(int i) throws IOException {
        print(i);
        print('\n');
    }

    public void println(String str) throws IOException {
        print(str);
        print('\n');
    }

    private byte read() throws IOException {
        if (bufferPointer == bytesRead) {
            fillBuffer();
        }
        return buffer[bufferPointer++];
    }

    private void fillBuffer() throws IOException {
        bytesRead = din.read(buffer, bufferPointer = 0, BUFFER_SIZE);
        if (bytesRead == -1) {
            buffer[0] = -1;
        }
    }

    private void flushBuffer() throws IOException {
        if (outPointer == 0) return;
        out.write(outBuffer, 0, outPointer);
        outPointer = 0;
    }

    public void flush() throws IOException {
        flushBuffer();
        out.flush();
    }

    public void close() throws IOException {
        flush();
        din.close();
        out.close();
    }
}
